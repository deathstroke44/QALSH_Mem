#!/bin/bash
make clean
make -j

# ------------------------------------------------------------------------------
#  Parameters ('dtype' has 4 options: uint8, uint16, int32, float32)
# ------------------------------------------------------------------------------
n=1000000
qn=10000
d=128
B=4096
c=2.0
leaf=20000
L=30
M=7
dtype=float32
dname=sift
pf=../data/${dname}/${dname}
df=../data/${dname}/
log=sift1+

# ------------------------------------------------------------------------------
#  Running Scripts
# ------------------------------------------------------------------------------
p=2.0
z=0.0

of=../results/${dname}/c=${c}_p=${p}/

# ----------------------------------------------------------------------------
#  QALSH+
# ----------------------------------------------------------------------------
of=../results/${dname}/c=${c}_p=${p}/


# ----------------------------------------------------------------------------
#  QALSH_Plus
# ----------------------------------------------------------------------------
./qalsh -alg 1 -n ${n} -qn ${qn} -d ${d} -p ${p} -z ${z} -c ${c} \
-lf ${leaf} -L ${L} -M ${M} -dt ${dtype} -pf ${pf} -of ${of} -dname ${log}


